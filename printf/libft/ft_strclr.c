#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;
	char	*str;

	str = (char*)s;
	i = 0;
	if (str)
	{
		while (str[i])
		{
			str[i] = '\0';
			i++;
		}
	}
}
